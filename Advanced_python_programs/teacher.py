# Create teacher class.
class teacher:
    def setid(self,id):
        self.id=id
    def getid(self):
        return self.id
    def setname(self,name):
        self.name=name
    def getname(self):
        return self.name
    def setage(self,age):
        self.age=age
    def getage(self):
        return self.age
    def setheight(self,height):
        self.height=height
    def getheight(self):
        return self.height
    def setweight(self,weight):
        self.weight=weight
    def getweight(self):
        return self.weight
    def setaddress(self,address):
        self.address=address
    def getaddress(self):
        return self.address
    def setsalory(self,salory):
        self.salory=salory
    def getsalory(self):
        return self.salory
